# Status and Monitoring
# ═══════════════════════════════════════════════════════════════════

show_status() {
    log_info "Epic 8 Deployment Status"
    echo ""

    load_state

    # Check ECS services
    log_info "ECS Services:"
    for service in api-gateway query-analyzer generator analytics; do
        status=$(aws ecs describe-services \
            --cluster $CLUSTER_NAME \
            --services $service \
            --query 'services[0].status' \
            --output text \
            --region $AWS_REGION 2>/dev/null || echo "NOT_FOUND")

        running=$(aws ecs describe-services \
            --cluster $CLUSTER_NAME \
            --services $service \
            --query 'services[0].runningCount' \
            --output text \
            --region $AWS_REGION 2>/dev/null || echo "0")

        desired=$(aws ecs describe-services \
            --cluster $CLUSTER_NAME \
            --services $service \
            --query 'services[0].desiredCount' \
            --output text \
            --region $AWS_REGION 2>/dev/null || echo "0")

        if [[ "$status" == "ACTIVE" && "$running" == "$desired" && "$running" != "0" ]]; then
            echo -e "  ${GREEN}✓${NC} $service: $running/$desired tasks"
        else
            echo -e "  ${RED}✗${NC} $service: $running/$desired tasks (status: $status)"
        fi
    done

    echo ""
    log_info "ALB Endpoint:"
    if [[ -n "$ALB_DNS" ]]; then
        echo "  http://$ALB_DNS"
        echo "  Health: http://$ALB_DNS/health"
    else
        echo "  Not available"
    fi

    echo ""
    log_info "Estimated Daily Cost: \$3.20"
}

show_logs() {
    load_state

    log_info "Select service to view logs:"
    echo "  1) api-gateway"
    echo "  2) query-analyzer"
    echo "  3) generator"
    echo "  4) analytics"
    read -p "Choice (1-4): " choice

    case $choice in
        1) service="api-gateway" ;;
        2) service="query-analyzer" ;;
        3) service="generator" ;;
        4) service="analytics" ;;
        *) log_error "Invalid choice"; exit 1 ;;
    esac

    log_info "Tailing logs for $service (Ctrl+C to exit)..."
    aws logs tail /ecs/${PROJECT_NAME}-${service} --follow --region $AWS_REGION
}

