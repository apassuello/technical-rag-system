# ═══════════════════════════════════════════════════════════════════
# Teardown
# ═══════════════════════════════════════════════════════════════════

teardown() {
    log_warn "This will delete ALL Epic 8 infrastructure. Are you sure? (yes/no)"
    read -r confirmation

    if [[ "$confirmation" != "yes" ]]; then
        log_info "Teardown cancelled"
        exit 0
    fi

    log_info "Starting teardown..."

    load_state

    # Delete ECS services
    for service in api-gateway query-analyzer generator analytics; do
        log_info "Deleting service: $service"
        aws ecs update-service \
            --cluster $CLUSTER_NAME \
            --service $service \
            --desired-count 0 \
            --region $AWS_REGION 2>/dev/null || true
        aws ecs delete-service \
            --cluster $CLUSTER_NAME \
            --service $service \
            --force \
            --region $AWS_REGION 2>/dev/null || true
    done

    # Wait for services to be deleted
    sleep 30

    # Delete ECS cluster
    log_info "Deleting ECS cluster"
    aws ecs delete-cluster \
        --cluster $CLUSTER_NAME \
        --region $AWS_REGION 2>/dev/null || true

    # Delete ALB
    if [[ -n "$ALB_ARN" ]]; then
        log_info "Deleting ALB"
        aws elbv2 delete-load-balancer \
            --load-balancer-arn $ALB_ARN \
            --region $AWS_REGION 2>/dev/null || true
    fi

    # Wait for ALB to be deleted
    sleep 30

    # Delete target groups
    if [[ -n "$TG_ARN" ]]; then
        log_info "Deleting target group"
        aws elbv2 delete-target-group \
            --target-group-arn $TG_ARN \
            --region $AWS_REGION 2>/dev/null || true
    fi

    # Delete VPC resources
    if [[ -n "$VPC_ID" ]]; then
        log_info "Deleting VPC resources"

        # Delete subnets
        if [[ -n "$SUBNET_ID" ]]; then
            aws ec2 delete-subnet --subnet-id $SUBNET_ID --region $AWS_REGION 2>/dev/null || true
        fi

        # Detach and delete IGW
        if [[ -n "$IGW_ID" ]]; then
            aws ec2 detach-internet-gateway --internet-gateway-id $IGW_ID --vpc-id $VPC_ID --region $AWS_REGION 2>/dev/null || true
            aws ec2 delete-internet-gateway --internet-gateway-id $IGW_ID --region $AWS_REGION 2>/dev/null || true
        fi

        # Delete security groups
        if [[ -n "$SG_ID" ]]; then
            aws ec2 delete-security-group --group-id $SG_ID --region $AWS_REGION 2>/dev/null || true
        fi

        # Delete route tables
        if [[ -n "$RTB_ID" ]]; then
            aws ec2 delete-route-table --route-table-id $RTB_ID --region $AWS_REGION 2>/dev/null || true
        fi

        # Delete VPC
        aws ec2 delete-vpc --vpc-id $VPC_ID --region $AWS_REGION 2>/dev/null || true
    fi

    # Delete CloudWatch log groups
    for service in api-gateway query-analyzer generator analytics; do
        log_info "Deleting log group: /ecs/${PROJECT_NAME}-${service}"
        aws logs delete-log-group \
            --log-group-name /ecs/${PROJECT_NAME}-${service} \
            --region $AWS_REGION 2>/dev/null || true
    done

    # Delete state file
    rm -f "$STATE_FILE"

    log_success "Teardown complete"
}

